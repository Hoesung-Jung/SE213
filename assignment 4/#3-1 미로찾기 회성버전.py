class Player:
    def __init__(self, x=0, y=0):
        self.x = x
        self.y = y
    def move(self, dir):
        if dir =='up':
            self.y += 1
        elif dir =='down':
            self.y -= 1
        elif dir =='left':
            self.x -= 1
        elif dir =='right':
            self.x += 1
        pass
    def get_position(self):
        return (self.x, self.y)

class MazeGame:
    def __init__(self, map=None):
        self.map = map
        self.player = Player()
        self.path = []


    def play(self):
        if self.player.get_position() == (len(self.map)-1, len(self.map)-1):
            return 1

        if self.player.get_position() in self.path:
            return
        self.path.append(self.player.get_position())
        self.direction = []
        try:
            if map[self.player.x][self.player.y + 1] == 0:
                self.direction.append('up')
        except:
            pass
        try:
            if map[self.player.x + 1][self.player.y] == 0:
                self.direction.append('right')
        except:
            pass
        try:
            if map[self.player.x][self.player.y - 1] == 0:
                self.direction.append('down')
        except:
            pass
        try:
            if map[self.player.x][self.player.y + 1] == 0:
                self.direction.append('left')
        except:
            pass

        for i in range(len(self.direction)):
            self.player.move('{0}'.format(self.direction[i]))
            self.play()




    def get_map(self):
        return self.map

    def get_path(self):
        return self.path


