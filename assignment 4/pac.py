class Player:
    def __init__(self, x=0, y=0):
        self.x = x
        self.y = y
    def move(self, dir):
        if dir =='up':
            self.y += 1
        elif dir =='down':
            self.y -= 1
        elif dir =='left':
            self.x -= 1
        elif dir =='right':
            self.x += 1
        pass
    def get_position(self):
        return (self.x, self.y)

class MazeGame:
    def __init__(self, map=None):
        self.map = map
        self.player = Player()
        self.path = []

    def play(self):
        i=j=0
        visited=[]
        queue=[]
        queue.append((j, i))
        #visited.append((i, j))
        while queue:
            #print(queue)
            n=queue.pop(0)
            if n not in visited:
                visited.append(n)
                if n[0]+1<len(self.map[0]) and self.map[n[0]+1][n[1]]==0:
                    queue.append((n[0]+1, n[1]))
                if n[1]+1<len(self.map) and self.map[n[0]][n[1]+1]==0:
                    queue.append((n[0], n[1]+1))
                if n[0]-1>=0 and self.map[n[0]-1][n[1]]==0:
                    queue.append((n[0]-1, n[1]))
                if n[1]-1>=0 and self.map[n[0]][n[1]-1]==0:
                    queue.append((n[0], n[1]-1))
            if visited[len(visited)-1]==(len(self.map[0])-1, len(self.map)-1):
                break
        final=[]
        for i in range(len(visited)):
            final.append((visited[i][1], visited[i][0]))
        ffinal=[]
        for idx in range(len(final)-1,-1,-1):
            if idx==len(final)-1:
                ffinal.append(final[idx])
            else:
                if (final[idx][0]==ffinal[-1][0] and abs(final[idx][1]-ffinal[-1][1])==1) or (final[idx][1]==ffinal[-1][1] and abs(final[idx][0]-ffinal[-1][0])==1):
                    ffinal.append(final[idx])
        ffinal.reverse()
        self.path=ffinal

    def get_map(self):
        return self.map
    def get_path(self):
        return self.path

