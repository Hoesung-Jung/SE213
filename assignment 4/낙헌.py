class Player:
    def __init__(self, x=0, y=0):
        self.x = x
        self.y = y
    def move(self, dir):
        if dir =='up':
            self.y += 1
        elif dir =='down':
            self.y -= 1
        elif dir =='left':
            self.x -= 1
        elif dir =='right':
            self.x += 1
        pass
    def get_position(self):
        return (self.x, self.y)

class MazeGame:
    def __init__(self, map=None):
        self.map = map
        self.player = Player()
        self.path = []

    def play(self):
        r = c = 0
        direction=[]
        qu=[]
        qu.append((c, r))
        while qu:
            n = qu.pop(0)
            if n not in direction:
                direction.append(n)
                if n[0]+1<len(self.map[0]) and self.map[n[0]+1][n[1]]==0:
                    qu.append((n[0]+1, n[1]))
                if n[1]+1<len(self.map) and self.map[n[0]][n[1]+1]==0:
                    qu.append((n[0], n[1]+1))
                if n[0]-1>=0 and self.map[n[0]-1][n[1]]==0:
                    qu.append((n[0]-1, n[1]))
                if n[1]-1>=0 and self.map[n[0]][n[1]-1]==0:
                    qu.append((n[0], n[1]-1))
            if direction[len(direction)-1]==(len(self.map[0])-1, len(self.map)-1):
                break
        path = []
        for i in range(len(direction)):
            path.append((direction[i][1], direction[i][0]))
        realpath=[]
        for idx in range(len(path)-1,-1,-1):
            if idx==len(path)-1:
                realpath.append(path[idx])
            else:
                if (path[idx][0]==realpath[-1][0] and abs(path[idx][1]-realpath[-1][1])==1) or (path[idx][1]==realpath[-1][1] and abs(path[idx][0]-realpath[-1][0])==1):
                    realpath.append(path[idx])
        realpath.reverse()
        self.path=realpath

    def get_map(self):
        return self.map
    def get_path(self):
        return self.path

